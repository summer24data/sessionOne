import java.util.Scanner;

public class choice{

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in) ;
		int cho = sc.nextInt();
		
		switch(cho) {

			case 1:
				System.out.println("1. Entered 1");
			break;
			case 2:
				System.out.println("2. Entered 2.");

		}
	}
}
